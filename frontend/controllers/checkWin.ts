export function checkWin(board: string[][], symbol: string) {
  const lines = [
    [
      [0, 0],
      [0, 1],
      [0, 2],
    ],
    [
      [1, 0],
      [1, 1],
      [1, 2],
    ],
    [
      [2, 0],
      [2, 1],
      [2, 2],
    ],
    [
      [0, 0],
      [1, 0],
      [2, 0],
    ],
    [
      [0, 1],
      [1, 1],
      [2, 1],
    ],
    [
      [0, 2],
      [1, 2],
      [2, 2],
    ],
    [
      [0, 0],
      [1, 1],
      [2, 2],
    ],
    [
      [0, 2],
      [1, 1],
      [2, 0],
    ],
  ];

  for (const ln of lines) {
    if (
      board[ln[0][0]][ln[0][1]] === symbol &&
      board[ln[1][0]][ln[1][1]] === symbol &&
      board[ln[2][0]][ln[2][1]] === symbol
    ) {
      return true;
    }
  }
}
